package worthywalk.example.com.worthywalk;

public class workout {
    int numsteps;
    float calories;
    float distance;
    float speed;
    int sec;
    int min;
    int hour;

    workout(int numsteps,float calories,float distance,float speed,int sec,int min,int hour) {
        this.numsteps=numsteps;
        this.calories=calories;
        this.distance=distance;
        this.speed=speed;
        this.sec=sec;
        this.min=min;
        this.hour=hour;
    }


}
